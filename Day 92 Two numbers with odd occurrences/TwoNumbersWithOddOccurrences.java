class Solution
{
    public int[] twoOddNum(int Arr[], int N)
    {
        int[] ans = new int[2];
        Map<Integer,Integer> map = new TreeMap<>();
        for(int i : Arr) {
            map.put(i,map.getOrDefault(i,0)+1);
        }
        int ind = 1;
        for(int i : map.keySet()) {
            if(map.get(i) % 2 != 0) {
                ans[ind--] = i;
            }
        }
        return ans;
    }
}
